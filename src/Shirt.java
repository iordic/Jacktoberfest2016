
public class Shirt {

    private int talla;

    public Shirt( int t ) {
        this.talla = t;
    }

    /**
     *
     * @return La talla de la camisa
     */
    public int getTalla() {
        return talla;
    }

    /**
     *
     * @param t La nueva talla de la camisa
     */
    public void setTalla( int t ) {
        this.talla = t;
    }

}
